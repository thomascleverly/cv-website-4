$(document).ready(function(){
    $('.admin-member').hide()
    $('.members-btn').click(function(){
        $('.admin-user').hide()
        $('.admin-member').fadeIn()
        $('.users-btn').removeClass('active')
        $('.members-btn').addClass('active')
    })
    $('.users-btn').click(function(){
        $('.admin-member').hide()
        $('.admin-user').fadeIn()
        $('.members-btn').removeClass('active')
        $('.users-btn').addClass('active')
    })
})